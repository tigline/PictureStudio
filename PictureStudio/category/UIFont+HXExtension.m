#import "UIFont+HXExtension.h"

@implementation UIFont (HXExtension)
+ (UIFont *)hx_pingFangFontOfSize:(CGFloat)size {
    return [UIFont fontWithName:@"PingFang-SC-Regular" size:size];
}
@end
